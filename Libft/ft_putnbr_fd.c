#include "libft.h"

static void	ft_putchar(char c, int fd)
{
	write(fd, &c, 1);
}

static void	ft_positif(long n, int fd)
{
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (n <= 9)
	{
		n += 48;
		ft_putchar(n, fd);
	}
}

void	ft_putnbr_fd(int n, int fd)
{
	long	nb;

	if (fd < 0)
		return ;
	nb = n;
	if (nb >= INT_MIN && nb <= INT_MAX)
	{
		if (nb < 0)
		{
			ft_putchar('-', fd);
			nb = -nb;
		}
		ft_positif(nb, fd);
	}
}
